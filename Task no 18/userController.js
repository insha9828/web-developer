import User from '../model/userSchema.js'
import dotenv from 'dotenv'
import bcrypt from 'bcrypt'
import jwt from 'jsonwebtoken';
dotenv.config()
const SECRET_KEY = process.env.SECRET_KEY

// ðŸ”¹ Signup
export const signupUser = async (req, res) => {
  const { name, email, password } = req.body;

  const exist = await User.findOne({ email });
  if (exist) return res.json({ message: "Email already exists " });

  const hashed = await bcrypt.hash(password, 10);
  const user = new User({ name, email, password: hashed });
  await user.save();

  res.json({ message: "Signup successful " });
};

// ðŸ”¹ Login
export const loginUser = async (req, res) => {
  const { email, password } = req.body;

  const user = await User.findOne({ email });
  if (!user) return res.json({ message: "User not found âŒ" });

  const isMatch = await bcrypt.compare(password, user.password);
  if (!isMatch) return res.json({ message: "Wrong password âŒ" });

  const token = jwt.sign({ id: user._id }, "secretkey", { expiresIn: "1h" });
  res.json({ message: "Login successful âœ…", token });
};

// ðŸ”¹ Profile (Protected)
export const getProfile = async (req, res) => {
  const user = await User.findById(req.userId).select("-password");
  res.json({ message: "Profile data âœ…", user });
};

